// Comparison operators

// == equal value
var a = 10
var b = 5
var c = '10'

console.log(a==b)
console.log(a==c)

// === Equal value and Equal type
console.log('----------------------')
var a = 10
var b = 5
var c = '10'

console.log(a===b)
console.log(a===c)

// != not equal value
console.log('----------------------')
var a = 10
var b = 5
var c = '10'

console.log(a!=b)
console.log(a!=c)

// === NOT Equal value and NOT Equal type
console.log('----------------------')

var a = 10
var b = 5
var c = '10'

console.log(a!==b)
console.log(a!==c)

// greater than
console.log('----------------------')
console.log(15 > 10)
// less than
console.log('----------------------')
console.log(5 < 10)

// greater than or equal to
console.log('----------------------')
console.log(15 >= 15)
console.log(15 >= 16)

// less than or equal to
console.log('----------------------')
console.log(15 <= 15)
console.log(15 <= 6)